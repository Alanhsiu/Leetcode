#!/usr/bin/env node
// Assemble the per-problem Standard Solution JSON files (scripts/std/<routeSlug>.json,
// written by the author-standard-solutions workflow) into the sharded TS data files
// (src/data/standard/s1..s4.ts) consumed by content.ts. Reports any files that failed
// to parse and any the workflow flagged (ok:false) so they can be re-run/reviewed.
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const STD_DIR = join(ROOT, "scripts", "std");
const SHARDS = 4;
const REQUIRED = ["routeSlug", "summary", "approach", "code", "timeComplexity", "spaceComplexity"];

const files = readdirSync(STD_DIR).filter((f) => f.endsWith(".json") && f !== "std-input.json");
const ok = [];
const bad = [];
const flagged = [];

for (const f of files) {
  const raw = readFileSync(join(STD_DIR, f), "utf8");
  let obj;
  try {
    obj = JSON.parse(raw);
  } catch (e) {
    bad.push({ file: f, error: String(e.message || e) });
    continue;
  }
  const missing = REQUIRED.filter((k) => typeof obj[k] !== "string" || !obj[k].length);
  if (missing.length) {
    bad.push({ file: f, error: `missing/empty: ${missing.join(", ")}` });
    continue;
  }
  if (obj.ok === false) flagged.push({ routeSlug: obj.routeSlug, issues: obj.issues || [] });
  ok.push({
    routeSlug: obj.routeSlug,
    summary: obj.summary,
    approach: obj.approach,
    code: obj.code,
    timeComplexity: obj.timeComplexity,
    spaceComplexity: obj.spaceComplexity,
  });
}

ok.sort((a, b) => a.routeSlug.localeCompare(b.routeSlug));

// distribute round-robin into shards
const buckets = Array.from({ length: SHARDS }, () => []);
ok.forEach((e, i) => buckets[i % SHARDS].push(e));

const lit = (s) => JSON.stringify(s);
for (let i = 0; i < SHARDS; i++) {
  const n = i + 1;
  const body = buckets[i]
    .map(
      (e) =>
        `  {\n` +
        `    routeSlug: ${lit(e.routeSlug)},\n` +
        `    summary: ${lit(e.summary)},\n` +
        `    approach: ${lit(e.approach)},\n` +
        `    code: ${lit(e.code)},\n` +
        `    timeComplexity: ${lit(e.timeComplexity)},\n` +
        `    spaceComplexity: ${lit(e.spaceComplexity)},\n` +
        `  },`,
    )
    .join("\n");
  const out =
    `import type { StandardSolution } from "../standardTypes";\n` +
    `// Shard ${n} of the AI-authored Standard Solutions (keyed by routeSlug). Generated by\n` +
    `// scripts/build-standards.mjs from scripts/std/*.json. See NEEDS_REVIEW.md.\n` +
    `export const S${n}: StandardSolution[] = [\n${body}\n];\n`;
  writeFileSync(join(ROOT, "src", "data", "standard", `s${n}.ts`), out);
}

console.log(`Assembled ${ok.length} standards into ${SHARDS} shards.`);
if (flagged.length) {
  console.log(`\n⚠ ${flagged.length} flagged (ok:false) by the verifier:`);
  for (const f of flagged) console.log(`  - ${f.routeSlug}: ${(f.issues || []).join("; ")}`);
}
if (bad.length) {
  console.log(`\n✗ ${bad.length} file(s) failed to parse/validate (re-run these):`);
  for (const b of bad) console.log(`  - ${b.file}: ${b.error}`);
  process.exitCode = 2;
}
